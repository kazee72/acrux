pub struct Token {
    pub kind: TokenKind,
    pub line: usize,
}

pub enum TokenKind {
    Affix,
    Crux,
    Vox,
    Apex,
    If,
    Else,
    While,
    For,
    Assign,
    True,
    False,
    Plus,
    Minus,
    Mult,
    Div,
    And,
    Or,
    Not,
    Gt,
    Lt,
    Eq,
    NotEq,
    GtEq,
    LtEq,
    Semicolon,
    Comma,
    LParen,
    RParen,
    LCurly,
    RCurly,
    Integer(i64),
    Float(f64),
    StringLit(String),
    Identifier(String),
    EOF,
    Unknown(char)
}